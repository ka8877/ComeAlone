package com.kosmo.alone.vo;

public class ProductsVO {
	
	private String monName;
	private String payRegdate;
	private int sseq;
	private int eseq;
	private int proPk;
	private String proName;
	private String proDetail;
	private int proPrice;
	private String proRegdate;
	
	public String getMonName() {
		return monName;
	}
	public void setMonName(String monName) {
		this.monName = monName;
	}
	public String getPayRegdate() {
		return payRegdate;
	}
	public void setPayRegdate(String payRegdate) {
		this.payRegdate = payRegdate;
	}
	public int getSseq() {
		return sseq;
	}
	public void setSseq(int sseq) {
		this.sseq = sseq;
	}
	public int getEseq() {
		return eseq;
	}
	public void setEseq(int eseq) {
		this.eseq = eseq;
	}
	public int getProPk() {
		return proPk;
	}
	public void setProPk(int proPk) {
		this.proPk = proPk;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getProDetail() {
		return proDetail;
	}
	public void setProDetail(String proDetail) {
		this.proDetail = proDetail;
	}
	public int getProPrice() {
		return proPrice;
	}
	public void setProPrice(int proPrice) {
		this.proPrice = proPrice;
	}
	public String getProRegdate() {
		return proRegdate;
	}
	public void setProRegdate(String proRegdate) {
		this.proRegdate = proRegdate;
	}
	
	
	
}
