package com.kosmo.alone.vo;

public class CategoriesVO {

	private int catePk;
	private String cateName;
	private String cateRegdate;
	private String cateGubun;
	
	public int getCatePk() {
		return catePk;
	}
	public void setCatePk(int catePk) {
		this.catePk = catePk;
	}
	public String getCateName() {
		return cateName;
	}
	public void setCateName(String cateName) {
		this.cateName = cateName;
	}
	public String getCateRegdate() {
		return cateRegdate;
	}
	public void setCateRegdate(String cateRegdate) {
		this.cateRegdate = cateRegdate;
	}
	public String getCateGubun() {
		return cateGubun;
	}
	public void setCateGubun(String cateGubun) {
		this.cateGubun = cateGubun;
	}
	
}
