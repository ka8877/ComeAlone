package com.kosmo.alone.vo;

public class ReplyVO {
	
	private int boaPk;
	private int monPk;
	private int repPk;
	private String repCon;
	private String repRegdate;
	private int rcnt;
	private String monName;
	
	public int getBoaPk() {
		return boaPk;
	}
	public void setBoaPk(int boaPk) {
		this.boaPk = boaPk;
	}
	public int getMonPk() {
		return monPk;
	}
	public void setMonPk(int monPk) {
		this.monPk = monPk;
	}
	public int getRepPk() {
		return repPk;
	}
	public void setRepPk(int repPk) {
		this.repPk = repPk;
	}
	public String getRepCon() {
		return repCon;
	}
	public void setRepCon(String repCon) {
		this.repCon = repCon;
	}
	public String getRepRegdate() {
		return repRegdate;
	}
	public void setRepRegdate(String repRegdate) {
		this.repRegdate = repRegdate;
	}
	public int getRcnt() {
		return rcnt;
	}
	public void setRcnt(int rcnt) {
		this.rcnt = rcnt;
	}
	public String getMonName() {
		return monName;
	}
	public void setMonName(String monName) {
		this.monName = monName;
	}
	
	

}
